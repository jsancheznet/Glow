struct renderer
{
    SDL_Window *Window;

    // Settings
    f32 Exposure;

    // OpenGL
    const u8 *HardwareVendor = NULL;
    const u8 *HardwareModel = NULL;
    const u8 *OpenGLVersion = NULL;
    const u8 *GLSLVersion = NULL;

    u32 DrawableWidth;
    u32 DrawableHeight;

    u32 QuadVAO;
    u32 QuadVBO;
    u32 CubeVAO;
    u32 CubeVBO;

    // Shaders
    u32 BlurShader;
    u32 BloomShader;
    u32 CubeShader;
    u32 HdrShader;

    // TODO:(Jorge): Rename and comment what each buffer/variable is for
    u32 Framebuffer;
    u32 ColorBuffer;
    u32 BrightnessBuffer;
    u32 DepthStencilRenderbuffer;
    u32 Attachments[2];
    u32 PingPongFBO[2];
    u32 PingPongBuffer[2];
};

struct character
{
    u32 TextureID;
    glm::ivec2 Size; // Size of glyph
    glm::ivec2 Bearing; // Offset from baseline to left/top of glyph
    u32 Advance; // Offset to advance to next glyph
};

struct font
{
    char *Filename;
    u32 Shader;
    i32 CharacterWidth;
    i32 CharacterHeight;

    character Characters[256];

    u32 VAO;
    u32 VertexBuffer;
    u32 TexCoordsBuffer;
};

